public class mdlConversor {
    public static float euro(float dolar){
        return dolar*0.86f;
    }
    
    public static float peso(float dolar){
        return dolar*22.28f;
    }
    
    public static float dolarC(float dolar){
        return dolar*1.34f;
    }
}
